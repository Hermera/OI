using namespace std;

const int N = 55;
class SpellCards {
    public:
    int n, dp[N];
    void cmax(int &x, int y) {
    	x = x < y ? y : x;
    }
    
    int maxDamage(vector <int> w, vector <int> v) {
        n = w.size();
        
        for(int i = 0; i < n; ++i) 
			for(int j = n; j >= w[i]; --j)
				cmax(dp[j], dp[j-w[i]] + v[i]);
        
        int ans = 0;
        for(int i = 1; i <= n; ++i)
        	cmax(ans, dp[i]);
        return ans;
    }
};

